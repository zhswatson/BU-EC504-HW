package hw2Q3a.ec504.bu;

public class Element {
	
	private int data;
	private int key;
	
	public Element(int data, int key) {
		super();
		this.data = data;
		this.key = key;
	}
	
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	

}
